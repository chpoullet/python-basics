from math import pi
# The task is to create functional calculator
# we need it to be able to, add, subtract, divide and multiply

def add(x, y):
    return x + y

def subtract(x, y):
    return x - y

def multiply(x, y):
    return x * y

def divide(x, y):
    return x / y

def area_of_circle(x):
    return (x * x) * pi

def area_of_square(x):
    return x * x

def area_of_triangle(x, y):
    return 0.5 * (x * y)


